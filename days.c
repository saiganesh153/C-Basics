#include<stdio.h>
void main()
{
	int a;
	printf("enter a number");
	scanf("%d",&a);
	if(a==1)
	printf("monday",a);
	else if(a==2)
	printf("tuesday",a);
	else if(a==3)
	printf("wednesday",a);
	else if(a==4)
	printf("thursday",a);
	else if(a==5)
	printf("friday",a);
	else if(a==6)
	printf("saturday",a);
	else if(a==7)
	printf("sunday",a);
	else
	printf("invalid");
	
	
}
